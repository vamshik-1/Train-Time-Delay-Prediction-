def Predict_Train_Delay_Type(request):
    if request.method == "POST":

        if request.method == "POST":
            RID = request.POST.get('RID')
            ddatetime= request.POST.get('ddatetime')
            idn= request.POST.get('idn')
            carrier= request.POST.get('carrier')
            adate1= request.POST.get('adate1')
            connection= request.POST.get('connection')
            arrival= request.POST.get('arrival')
            delay_in_min= request.POST.get('delay_in_min')
            name= request.POST.get('name')



        df = pd.read_csv('Train_Datasets.csv')

        def apply_response(delay):
            if (int(delay) >=int(0) and  int(delay) <= int(5)):
                return 0  # No Delay
            elif (int(delay)> int(5)):
                return 1  # More Delay

        df['results'] = df['delay_in_min'].apply(apply_response)

        cv = CountVectorizer()
        X = df['RID']
        y = df['results']

        print("Connection Station")
        print(X)
        print("Results")
        print(y)

        X = cv.fit_transform(X)

        models = []
        from sklearn.model_selection import train_test_split
        X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.20)
        X_train.shape, X_test.shape, y_train.shape

        print("Naive Bayes")

        from sklearn.naive_bayes import MultinomialNB

        NB = MultinomialNB()
        NB.fit(X_train, y_train)
        predict_nb = NB.predict(X_test)
        naivebayes = accuracy_score(y_test, predict_nb) * 100
        print("ACCURACY")
        print(naivebayes)
        print("CLASSIFICATION REPORT")
        print(classification_report(y_test, predict_nb))
        print("CONFUSION MATRIX")
        print(confusion_matrix(y_test, predict_nb))
        models.append(('naive_bayes', NB))

        # SVM Model
        print("SVM")
        from sklearn import svm

        lin_clf = svm.LinearSVC()
        lin_clf.fit(X_train, y_train)
        predict_svm = lin_clf.predict(X_test)
        svm_acc = accuracy_score(y_test, predict_svm) * 100
        print("ACCURACY")
        print(svm_acc)
        print("CLASSIFICATION REPORT")
        print(classification_report(y_test, predict_svm))
        print("CONFUSION MATRIX")
        print(confusion_matrix(y_test, predict_svm))
        models.append(('svm', lin_clf))

        print("Logistic Regression")

        from sklearn.linear_model import LogisticRegression

        reg = LogisticRegression(random_state=0, solver='lbfgs').fit(X_train, y_train)
        y_pred = reg.predict(X_test)
        print("ACCURACY")
        print(accuracy_score(y_test, y_pred) * 100)
        print("CLASSIFICATION REPORT")
        print(classification_report(y_test, y_pred))
        print("CONFUSION MATRIX")
        print(confusion_matrix(y_test, y_pred))
        models.append(('logistic', reg))

        print("SGD Classifier")
        from sklearn.linear_model import SGDClassifier
        sgd_clf = SGDClassifier(loss='hinge', penalty='l2', random_state=0)
        sgd_clf.fit(X_train, y_train)
        sgdpredict = sgd_clf.predict(X_test)
        print("ACCURACY")
        print(accuracy_score(y_test, sgdpredict) * 100)
        print("CLASSIFICATION REPORT")
        print(classification_report(y_test, sgdpredict))
        print("CONFUSION MATRIX")
        print(confusion_matrix(y_test, sgdpredict))
        models.append(('SGDClassifier', sgd_clf))

        classifier = VotingClassifier(models)
        classifier.fit(X_train, y_train)
        y_pred = classifier.predict(X_test)

        RID1 = [RID]
        vector1 = cv.transform(RID1).toarray()
        predict_text = classifier.predict(vector1)

        pred = str(predict_text).replace("[", "")
        pred1 = pred.replace("]", "")

        prediction = int(pred1)

        if (prediction == 0):
            val = 'No Delay'
        elif (prediction == 1):
            val = 'More Delay'

        print(val)
        print(pred1)

        delay_prediction_type.objects.create(ddatetime=ddatetime,
        idn=idn,
        carrier=carrier,
        adate1=adate1,
        connection=connection,
        arrival=arrival,
        delay_in_min=delay_in_min,
        name=name,
        Prediction=val)

        return render(request, 'RUser/Predict_Train_Delay_Type.html',{'objs': val})
    return render(request, 'RUser/Predict_Train_Delay_Type.html')



